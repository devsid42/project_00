#include "libft.h"

void	ft_putstr_d(const char *s)
{
	int		i;

	if (s != NULL)
	{
		i = 0;
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i++;
		}
	}
	else
		ft_putstr("(putstr_d null)");
}
